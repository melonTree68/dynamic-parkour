"""Dynamic terrain suite/layout definitions.

Layouts are deliberately data-only: the manager turns these obstacle specs into
Isaac Gym box actors while keeping static heightfields and triangle meshes
unchanged.
"""

DYNAMIC_TERRAIN_SHARED_FREQUENCY_RANGES = {
    "moving_hurdle": [0.08, 0.22],
    "changing_step_height": [0.05, 0.20],
    "shifting_gap": [0.05, 0.20],
    "time_varying_ramp": [0.05, 0.18],
}


def hurdle(
    name, x, y, z, axis, amp, freq, phase, length=1.0, thickness=0.08, height=0.35
):
    return {
        "name": name,
        "type": "moving_hurdle",
        "actor_count": 1,
        "base_position": [x, y, z],
        "size": [length, thickness, height],
        "motion_axis": axis,
        "amplitude_range": amp,
        "frequency_range": freq,
        "phase_range": phase,
    }


def step(name, x, y, z, amp, freq, phase, length=0.6, width=1.0, height=0.18):
    center_z = z - 0.6 * height
    return {
        "name": name,
        "type": "changing_step_height",
        "actor_count": 1,
        "base_position": [x, y, center_z],
        "size": [length, width, height],
        "motion_axis": "z",
        "amplitude_range": amp,
        "frequency_range": freq,
        "phase_range": phase,
        "step_height": height,
        "emerges_from_ground": True,
        "nominal_exposed_height": z,
    }


def gap(
    name,
    x,
    y,
    z,
    axis,
    amp,
    freq,
    phase,
    separation=1.20,
    length=0.55,
    width=1.15,
    height=0.10,
    gap_motion="width",
):
    return {
        "name": name,
        "type": "shifting_gap",
        "actor_count": 2,
        "base_position": [x, y, z],
        "size": [length, width, height],
        "motion_axis": axis,
        "gap_motion": gap_motion,
        "edge_separation": separation,
        "amplitude_range": amp,
        "frequency_range": freq,
        "phase_range": phase,
    }


def ramp(
    name,
    x,
    y,
    z,
    amp,
    freq,
    phase,
    base_pitch=0.15,
    axis="roll",
    length=1.35,
    width=1.15,
    thickness=0.045,
):
    return {
        "name": name,
        "type": "time_varying_ramp",
        "actor_count": 1,
        "base_position": [x, y, z],
        "size": [length, width, thickness],
        "motion_axis": axis,
        "rotation_axis_design": "parallel_to_robot_forward",
        "base_pitch": base_pitch,
        "amplitude_range": amp,
        "frequency_range": freq,
        "phase_range": phase,
    }


def _obstacle_x_extent(obstacle):
    x, _y, _z = obstacle["base_position"]
    length = obstacle["size"][0]
    if obstacle["type"] == "shifting_gap":
        half_x = 0.5 * obstacle["edge_separation"] + 0.5 * length
    else:
        half_x = 0.5 * length
    return x - half_x, x + half_x


def _default_goals(obstacles, runup_length, runout_length):
    goals = [[0.45, 0.0]]
    last_x = goals[0][0]

    for obstacle in obstacles:
        x, y, _z = obstacle["base_position"]
        x0, x1 = _obstacle_x_extent(obstacle)
        if obstacle["type"] == "shifting_gap":
            goals.append([round(x0 - 0.10, 3), round(y, 3)])
            goals.append([round(x1 + 0.10, 3), round(y, 3)])
        else:
            approach_x = max(last_x + 0.35, x0 - 0.20)
            goals.append([round(approach_x, 3), round(y, 3)])
            goals.append([round(x1 + 0.20, 3), round(y, 3)])
        last_x = goals[-1][0]

    final_x = max(last_x + 0.45, _obstacle_x_extent(obstacles[-1])[1] + runout_length)
    goals.append([round(final_x, 3), 0.0])
    return goals


def course_layout(
    name,
    difficulty,
    runup_length,
    runout_length,
    corridor_half_width,
    obstacles,
    goals=None,
):
    return {
        "name": name,
        "difficulty": difficulty,
        "runup_length": runup_length,
        "runout_length": runout_length,
        "corridor_half_width": corridor_half_width,
        "obstacles": obstacles,
        "goals": (
            goals
            if goals is not None
            else _default_goals(obstacles, runup_length, runout_length)
        ),
    }


